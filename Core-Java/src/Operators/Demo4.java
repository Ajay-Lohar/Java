package Operators;

public class Demo4 {
    public static void main(String[] args) {

        int number = 2345 ;
        // Addition
        number += 8;//number =number + 8
        System.out.println(number);// 2353

        //Subtractraction
        number =number - 8 ;
        System.out.println(number); //2337

        //Division
        number /= 8 ;
        System.out.println(number);//293

        //Multiplication
        number *= 8 ;
        System.out.println(number);//18760
    }
}
